import { test } from "@perspective-dev/test";
import { gotoBasic, renderAndCapture } from "./helpers";

test.describe("X Bar", () => {
    test.beforeEach(async ({ page }) => {
        await gotoBasic(page);
    });

    test("basic single series", async ({ page }) => {
        await renderAndCapture(
            page,
            {
                plugin: "X Bar",
                columns: ["Sales"],
                group_by: ["Category"],
            },
            "basic.png",
        );
    });

    test("split_by series colors", async ({ page }) => {
        await renderAndCapture(
            page,
            {
                plugin: "X Bar",
                columns: ["Sales"],
                group_by: ["Category"],
                split_by: ["Region"],
            },
            "split_by.png",
        );
    });

    test("multiple X columns", async ({ page }) => {
        await renderAndCapture(
            page,
            {
                plugin: "X Bar",
                columns: ["Sales", "Profit"],
                group_by: ["Category"],
            },
            "multi-x.png",
        );
    });

    test("nested group_by", async ({ page }) => {
        await renderAndCapture(
            page,
            {
                plugin: "X Bar",
                columns: ["Sales"],
                group_by: ["Region", "Category"],
            },
            "nested-group.png",
        );
    });
});
