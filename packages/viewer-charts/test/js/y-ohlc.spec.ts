import { test } from "@perspective-dev/test";
import { gotoBasic, renderAndCapture } from "./helpers";

test.describe("Y OHLC", () => {
    test.beforeEach(async ({ page }) => {
        await gotoBasic(page);
    });

    test("basic four-column OHLC", async ({ page }) => {
        await renderAndCapture(page, {
            plugin: "OHLC",
            columns: ["Sales", "Profit", "Quantity", "Discount"],
            group_by: ["Order Date"],
        });
    });

    test("open-only falls back to next-row open", async ({ page }) => {
        await renderAndCapture(page, {
            plugin: "OHLC",
            columns: ["Sales"],
            group_by: ["Order Date"],
        });
    });

    test("with split_by", async ({ page }) => {
        await renderAndCapture(page, {
            plugin: "OHLC",
            columns: ["Sales"],
            group_by: ["Category"],
            split_by: ["Region"],
        });
    });
});
