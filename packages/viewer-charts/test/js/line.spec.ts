import { test } from "@perspective-dev/test";
import { gotoBasic, renderAndCapture } from "./helpers";

test.describe("X/Y Line", () => {
    test.beforeEach(async ({ page }) => {
        await gotoBasic(page);
    });

    test("basic x/y", async ({ page }) => {
        await renderAndCapture(page, {
            plugin: "X/Y Line",
            columns: ["Order Date", "Profit"],
            group_by: ["Order Date"],
        });
    });

    test("split_by produces distinct series colors", async ({ page }) => {
        await renderAndCapture(page, {
            plugin: "X/Y Line",
            columns: ["Order Date", "Profit"],
            group_by: ["Order Date"],
            split_by: ["Category"],
        });
    });

    test("date X axis with two splits", async ({ page }) => {
        await renderAndCapture(page, {
            plugin: "X/Y Line",
            columns: ["Order Date", "Sales"],
            group_by: ["Order Date"],
            split_by: ["Region"],
        });
    });
});
