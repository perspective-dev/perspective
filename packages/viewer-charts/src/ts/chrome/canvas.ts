import type { PlotLayout } from "../layout/plot-layout";

/**
 * Resize a 2D canvas to CSS pixels scaled by DPR and return a pre-cleared,
 * DPR-scaled context. Returns null if the 2D context cannot be obtained.
 */
export function initCanvas(
    canvas: HTMLCanvasElement,
    layout: PlotLayout,
): CanvasRenderingContext2D | null {
    const dpr = window.devicePixelRatio || 1;
    canvas.width = Math.round(layout.cssWidth * dpr);
    canvas.height = Math.round(layout.cssHeight * dpr);
    const ctx = canvas.getContext("2d");
    if (!ctx) return null;
    ctx.scale(dpr, dpr);
    ctx.clearRect(0, 0, layout.cssWidth, layout.cssHeight);
    return ctx;
}
