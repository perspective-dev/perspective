//! Auxiliary types used by `ColumnConfigSchema`'s composite control
//! variants. Per-column persisted state itself is opaque
//! `serde_json::Map<String, serde_json::Value>`; see
//! [`crate::presentation::ColumnConfigMap`].

use serde::{Deserialize, Serialize};

#[derive(Serialize, Deserialize, Debug, PartialEq, Clone, Copy)]
#[serde(rename_all = "snake_case")]
pub enum KvPairKeyValue {
    Row,
}

#[derive(Serialize, Deserialize, Debug, PartialEq, Clone)]
#[serde(untagged)]
pub enum KvPairKeys {
    String(KvPairKeyValue),
    Vec(Vec<String>),
}

#[derive(Serialize, Deserialize, Debug, PartialEq, Clone)]
pub struct KeyValueOpts {
    pub keys: KvPairKeys,
    pub values: Vec<String>,
}

pub type SymbolKVPair = super::kvpair::KVPair<Option<String>, String>;
