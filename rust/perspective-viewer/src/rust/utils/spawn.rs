use std::future::Future;

use perspective_js::utils::*;

/// Spawn a fire-and-forget task whose ERRORS ARE OWNED: a failure lands in a
/// `tracing::error` tagged with `tag` instead of a dangling `Promise`
/// rejection (which `ApiFuture::spawn` produces, invisibly to callers and
/// tests). "View already deleted" cancellations are not failures.
///
/// Prefer RETURNING the future so the caller owns completion and error
/// (invariant I6); use this only at event-listener leaves where no caller
/// exists.
pub fn spawn_owned(tag: &'static str, fut: impl Future<Output = ApiResult<()>> + 'static) {
    ApiFuture::spawn(async move {
        if let Err(e) = fut.await.ignore_view_delete() {
            tracing::error!("[{}] unhandled task error: {}", tag, e);
        }

        Ok(())
    });
}
